import { mkdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import openapiTS from 'openapi-typescript'

const specEnvMap = {
    auth: 'OPENAPI_AUTH_URL',
    content: 'OPENAPI_CONTENT_URL',
    review: 'OPENAPI_REVIEW_URL',
    search: 'OPENAPI_SEARCH_URL',
    file: 'OPENAPI_FILE_URL',
}

const outputPath = path.resolve('src/shared/api/generated/openapi.ts')

async function renderNamespace(name, sourceUrl) {
    const schema = await openapiTS(new URL(sourceUrl))
    return `export namespace ${name} {\n${schema}\n}\n`
}

async function main() {
    const entries = Object.entries(specEnvMap)
        .map(([name, envKey]) => ({
            name,
            url: process.env[envKey],
        }))
        .filter((entry) => entry.url)

    if (entries.length === 0) {
        throw new Error(
            `No OpenAPI sources configured. Set at least one of: ${Object.values(specEnvMap).join(', ')}`,
        )
    }

    const rendered = await Promise.all(
        entries.map((entry) => renderNamespace(entry.name, entry.url)),
    )

    await mkdir(path.dirname(outputPath), { recursive: true })
    await writeFile(
        outputPath,
        `/* eslint-disable */\n// Generated by scripts/generate-api-types.mjs\n\n${rendered.join('\n')}`,
        'utf8',
    )
}

main().catch((error) => {
    console.error('[generate-api-types] failed:', error instanceof Error ? error.message : error)
    process.exitCode = 1
})
